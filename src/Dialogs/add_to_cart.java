    private void add_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_add_buttonMouseClicked
        try {
            int quantity = Integer.parseInt(quantity_field.getText().trim());

            if (quantity <= 0) {
                JOptionPane.showMessageDialog(this, "Quantity must be greater than zero.");
                return;
            }

            if (quantity > max_stocks) {
                JOptionPane.showMessageDialog(this, "Only " + max_stocks + " items available.");
                return;
            }

            db_connector db = new db_connector();
            String checkSql = "SELECT quantity FROM cart WHERE user_id = " + Session.getInstance().getUserId() + " AND product_id = " + productId;
            ResultSet rs = db.getData(checkSql);

            if (rs.next()) {
                int existingQty = rs.getInt("quantity");
                int newQty = existingQty + quantity;

                if (newQty > max_stocks) newQty = max_stocks;

                String updateSql = "UPDATE cart SET quantity = " + newQty + " WHERE user_id = " + Session.getInstance().getUserId() + " AND product_id = " + productId;
                db_connector.updateDatabase(updateSql);
            } else {
                String insertSql = "INSERT INTO cart (user_id, product_id, quantity) VALUES (" + Session.getInstance().getUserId() + ", " + productId + ", " + quantity + ")";
                db_connector.updateDatabase(insertSql);
            }

            CustomMessageDialog.showMessage(null, "Added to cart!", "Cart");
            this.dispose();

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Enter a valid number.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
        }
    }//GEN-LAST:event_add_buttonMouseClicked

