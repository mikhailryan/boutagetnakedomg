    private void loadInfo(){
        try {
            ResultSet rs = conn.getData("SELECT name, price, stock FROM products WHERE id = " + productId);
            if (rs.next()) {
                product_name.setText(rs.getString("name"));
                price_label.setText("â‚±" + rs.getString("price"));
                max_stocks = rs.getInt("stock");
            }  
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }
    
    private ImageIcon resizeImage(String path, JLabel label) {
        ImageIcon icon = new ImageIcon(getClass().getResource(path));
        Image img = icon.getImage().getScaledInstance(label.getWidth() - 10, label.getHeight() - 10, Image.SCALE_SMOOTH);
        return new ImageIcon(img);
    }
    
    private void updateQuantity(JTextField quantityField, int change, int maxStock) {
        try {
            int current = Integer.parseInt(quantityField.getText().trim());
            int updated = current + change;

            if (updated < 1) updated = 1;
            if (updated > maxStock) updated = maxStock;

            quantityField.setText(String.valueOf(updated));
        } catch (NumberFormatException e) {
            quantityField.setText("1");
        }
    }

